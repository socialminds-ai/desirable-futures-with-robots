  <section class="section section--whatif" id="whatif">
    <div class="section__marker"><span class="numeral">V</span><span class="label">What If</span></div>

    <h2 class="section__title">Reversing the narratives.</h2>

    <div class="prose prose--narrow">
      <p>
        Each workshop opens with a <em>what if</em> that reverses one element of the dominant framing — and asks participants to imagine a desirable future growing from there. Not dystopia. Not subversion for its own sake. A future they would actually want.
      </p>
    </div>

    <ol class="whatifs">
      <li class="whatif">
        <span class="whatif__num">01</span>
        <p class="whatif__text">What if robots <em>didn't always say yes</em>, but <em>questioned</em> you?</p>
      </li>
      <li class="whatif">
        <span class="whatif__num">02</span>
        <p class="whatif__text">What if a robot was <em>clumsy</em>, and sometimes needed our <em>help</em>?</p>
      </li>
      <li class="whatif">
        <span class="whatif__num">03</span>
        <p class="whatif__text">What if a robot didn't try to <em>save</em> your time, but to <em>waste it well</em>?</p>
      </li>
      <li class="whatif">
