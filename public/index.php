       I — THE URGENCY
       ============================================================ -->
  <section class="section section--urgency" id="urgency">
    <div class="section__marker"><span class="numeral">I</span><span class="label">The Urgency</span></div>

    <h2 class="section__title">From laboratory to industry.</h2>

    <div class="prose">
      <p class="lead">
        Robotics is shifting fast. In the past three years, parts of the field — humanoid platforms most visibly, but also home, delivery, service and care robots — have moved from laboratory subfields to industry-led races, with multi-trillion-dollar market forecasts and sizeable state investment in China, South Korea, and elsewhere. Capable, affordable platforms are reaching the market.
      </p>
      <p>
        Within one to two years, autonomous and semi-autonomous robots — humanoid or otherwise — are expected to be present in everyday life: at home, at work, in care, in public space.
      </p>
      <p class="emphasis">
        We collectively have a narrow window in which to shape the discourse.
      </p>
    </div>
  </section>


  <!-- ============================================================
       II — THE DOMINANT NARRATIVE
       ============================================================ -->
  <section class="section section--dark" id="dominant">
    <div class="section__marker section__marker--inverted"><span class="numeral">II</span><span class="label">The Dominant Narrative</span></div>

    <h2 class="section__title">Efficiency. Productivity. Replacement.</h2>

    <div class="prose">
      <p>
        The prevailing framing for autonomous and social robotics rests on a stable set of arguments: robots will perform jobs that are dull, dirty, or dangerous; they will offset labour shortages; they will save human time; and — most visibly through the humanoid wave — they will represent the next stage of the AI race, a step from text and image into <em>physical AI</em>.
      </p>
      <p>
        Capital momentum amplifies these claims. Industry forecasts place the humanoid segment alone on a multi-trillion-dollar trajectory; state-backed programmes in China, South Korea, and elsewhere position robotics as a strategic frontier. The discourse is largely shaped by industry actors and media, with limited HRI presence.
      </p>
      <p class="pullquote">
        Across these arguments runs a consistent through-line: robots are positioned in <strong>competition with humans</strong> — on labour, on time, on capability.
      </p>
      <p>
        The early effects of letting this framing harden are visible in generative AI. With embodied, autonomous robots — humanoid or otherwise — the same logic extends into domestic, relational, civic, and intimate domains, settings where the criteria of <em>efficiency</em> and <em>replacement</em> translate poorly.
      </p>

      <p>
      Alternative narratives exist: companion robots, procuring companionship, emotional support, wellbeing, and social connection. Robots positioned as responses to loneliness, ageing populations, mental health challenges, and care deficits.
      </p>

      <p>
    While this framing appears distinct from the competitive logic that characterises much of the robotics sector, it similarly carries assumptions about what kinds of human needs can be delegated, supplemented, or mediated through machines. As companion robots move from the margins toward the mainstream, the desirability of this vision deserves equal attention. 
      </p>
    </div>
  </section>


  <!-- ============================================================
       III — THE PROPOSITION
       ============================================================ -->
  <section class="section section--proposition" id="proposition">
    <div class="section__marker"><span class="numeral">III</span><span class="label">The Proposition</span></div>

    <h2 class="section__title">Other narratives are possible. And desirable.</h2>

    <div class="prose">
      <p class="lead">
        Without explicit debate, much of robotics has begun to converge on a narrow vision — robots as efficient surrogates for human labour, with the humanoid form as the most visible default. The convergence is largely silent: never formally decided, never ethically justified, sustained by media coverage, capital, and funding incentives.
      </p>
      <p>
        It deserves to be examined. <em>Human-centred</em> design is not the same as <em>human-shaped</em> design — and the question is not only how to design a robot, but whether and when a robot is the right answer at all. The robotics community has, at minimum, a responsibility to keep these distinctions live, and, where possible, to <strong>reclaim the narrative</strong>.
      </p>
      <p>
      This series of workshops is an effort to <strong>imagine alternative narratives</strong> of our future and where robots fit in — if at all. We ask: 
      </p>
    </div>

    <figure class="featured-question">
      <blockquote>
        Can we invent a caring,<br />
        feminist, <em>humanist</em><br />
        narrative for robots in our society?
      </blockquote>
      <figcaption>The question this series sets out to answer — together, in public, and from below.</figcaption>
    </figure>
  </section>


