    <div class="section__marker"><span class="numeral">IV</span><span class="label">The Series</span></div>

    <h2 class="section__title">Toward one hundred workshops. Distributed.</h2>

    <div class="series-grid">

      <div class="series-stat">
        <span class="series-stat__num">~100</span>
        <p>workshops — our target for the series. Reaching that number depends on researchers across the HRI community and beyond stepping in.</p>
      </div>

      <div class="series-stat">
        <span class="series-stat__num">∞</span>
        <p>publics: children, adolescents, older adults, warehouse workers, care workers, students, families — across cultures and socio-economic contexts.</p>
      </div>

      <div class="series-stat">
        <span class="series-stat__num">1</span>
        <p>shared reporting format, so results compare, aggregate, and accumulate into something larger than any single workshop.</p>
      </div>

      <div class="series-map">
        <div id="facilitator-map" class="facilitator-map" role="img" aria-label="World map showing where participating facilitators are based"></div>
        <noscript><img src="assets/map.svg" alt="A stylised world map." /></noscript>
        </div>
      </div>

      <div class="series-narrative">
        <p>
          Workshops are led by <strong>researchers acting as local facilitators</strong>. Protocols can vary by audience — role-play, drawing, brainstorming, interviews — but every workshop reports back in the same form, so the collective picture remains legible.
        </p>
        <p>
          These workshops <em>do not replace</em> long-term in-situ deployment. They do something different, and necessary: they plant alternative seeds, in many soils, before the dominant narratives finish setting.
        </p>
      </div>
    </div>
  </section>


